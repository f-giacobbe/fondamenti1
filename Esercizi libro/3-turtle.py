import turtle
window = turtle.Screen()    #opens up a window
ciccio = turtle.Turtle()    #creates a turtle, named 'ciccio'

ciccio.forward(50)      #makes ciccio move forward by 50 units
ciccio.left(90)     #makes ciccio rotate left by 90 degrees
ciccio.forward(30)

window.mainloop()       #wait for user to close window